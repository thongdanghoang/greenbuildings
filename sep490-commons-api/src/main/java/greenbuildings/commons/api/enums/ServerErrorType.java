package greenbuildings.commons.api.enums;

/**
 * Enum representing the type of server errors.
 * <p>
 * This enum has two types of server errors:
 * <ul>
 * <li>TECHNICAL: Represents technical errors that occur on the server side.</li>
 * <li>BUSINESS: Represents business logic related errors.</li>
 * </ul>
 */
public enum ServerErrorType {
    TECHNICAL,
    BUSINESS
}
